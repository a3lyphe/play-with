// bind keyboard events to game functions (movement, etc)
function bindKeys() {
	document.onkeydown=function(e) {
		e=e || window.event;
		switch (e.keyCode){// which key was pressed?
				case 88://'X',move player2 forward,ie. increase speed
				Zoom(1);
				break;
				case 89://'Y',move player2 forward,ie. increase speed
				Zoom(-1);
				break;
			case 87: // up, move player forward, ie. increase speed
				player.speed=1;
				break;
			case 65: // left, rotate player left
				player.dir=-1;
				break;
			case 68: // right, rotate player right
				player.dir=1;
				break;
		}
	}
	document.onkeyup=function(e){
		e=e || window.event;
		switch (e.keyCode) {
			case 87:
				player.speed=0;	// stop the player movement when up/down key is released
				break;
			case 65:
			case 68:
				player.dir=0;
				break;
		}
	}
}
function gameCycle(){
	Berechnung();
	//Visualisierung();
}
function Berechnung(){
	var moveStep=player.speed * player.moveSpeed;	// player will move this far along the current direction vector
	player.rot += player.dir * player.rotSpeed * Math.PI / 180; // add rotation if player is rotating (player.dir != 0)
	var newX=player.x + Math.cos(player.rot) * moveStep;	// calculate new player position with simple trigonometry
	var newY=player.y + Math.sin(player.rot) * moveStep;
	if (isBlocking(newX, newY)) {	// are we allowed to move to the new position?
		return; // no, bail out.
	}
	player.x=newX; // set new position
	player.y=newY;
}
function isBlocking(x,y){
	// first make sure that we cannot move outside the boundaries of the level
	if (y < 0 || y > mapHeight || x < 0 || x > mapWidth)
		return true;
	// return true if the map block is not 0, ie. if there is a blocking wall.
	return (map[Math.floor(y)][Math.floor(x)] != 0); 
}
